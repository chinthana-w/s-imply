"""PyTorch Dataset for reconvergent path pairs with embeddings.

This module provides a Dataset class compatible with the training pipeline in
train_reconv.py. It loads reconvergent path pairs generated by reconv_podem.py
and prepares them for training the Multi-Path transformer model.
"""

from __future__ import annotations

import glob
import json
import os
import pickle
from functools import lru_cache
from typing import Any, Dict, List, Optional, Tuple

import torch
from torch.utils.data import Dataset
from tqdm import tqdm

from src.util.io import parse_bench_file


class ReconvergentPathsDataset(Dataset):
    """Dataset for reconvergent path pairs with structural embeddings.

    Each sample contains:
    - paths_emb: [P, L, D] tensor of node embeddings along P paths of length L
    - attn_mask: [P, L] boolean mask indicating valid positions
    - node_ids: [P, L] tensor of node IDs for constraint checking
    - file: string path to the circuit file

    Parameters
    ----------
    dataset_path : str
        Path to the pickle file containing the dataset.
    device : torch.device
        Device to place tensors on (cpu or cuda).
    prefer_value : int
        Preferred value for initialization (unused in current implementation).
    max_path_length : int
        Maximum path length to pad/truncate to.
    """

    def __init__(
        self,
        dataset_path: str,
        device: torch.device = torch.device("cpu"),
        prefer_value: int = 1,
        max_path_length: int = 50,
        # Optional fast path: use preprocessed on-disk shards with tensors
        processed_dir: Optional[str] = None,
        load_processed: bool = False,
        add_logic_value: bool = True,
        anchor_in_dataset: bool = False,
        inject_constraints: bool = False,
        constraint_prob: float = 0.5,
        max_len_filter: int = 0,
        cache_size: int = 1,
    ):
        self.dataset_path = dataset_path
        self.device = device
        self.prefer_value = prefer_value
        self.max_path_length = max_path_length
        self.processed_dir = processed_dir
        self.load_processed = load_processed
        self.add_logic_value = add_logic_value
        self.anchor_in_dataset = anchor_in_dataset
        self.inject_constraints = inject_constraints
        self.constraint_prob = constraint_prob
        self.max_len_filter = max_len_filter
        self.cache_size = cache_size
        self._prefer_value = int(prefer_value)

        # Disable processed mode if filtering is active (for simplicity)
        if self.max_len_filter > 0 and self.load_processed:
            print(
                f"Disabling load_processed because max_len_filter={self.max_len_filter} is active."
            )
            self.load_processed = False

        # If using processed mode and cache exists, set up shard index and return
        self._shard_files: List[str] = []
        self._shard_sizes: List[int] = []
        self._current_shard_id: Optional[int] = None
        self._current_shard: Optional[Dict[str, Any]] = None

        # Default to not using processed shards; set True below if shards are found
        self._use_processed = False

        if self.load_processed and self.processed_dir and os.path.isdir(self.processed_dir):
            shard_paths = sorted(
                [
                    os.path.join(self.processed_dir, f)
                    for f in os.listdir(self.processed_dir)
                    if f.startswith("shard_") and f.endswith(".pt")
                ]
            )
            if shard_paths:
                self._load_shard_metadata(shard_paths)
                self._use_processed = True
                print(
                    f"Initialized lazy loading for {len(shard_paths)} shards, "
                    f"{self._total_len} samples"
                )
            else:
                print(
                    f"[WARNING] No shards found in {self.processed_dir}. Will fallback to pickle."
                )
        # Constraints are now baked into shards — no longer need pickle for inject_constraints
        self.need_pickle_info = not self._use_processed

        # Load raw pickle dataset if needed
        if self.need_pickle_info:
            import time

            print(f"Loading metadata/info from {dataset_path}...", flush=True)
            start_time = time.time()
            with open(dataset_path, "rb") as f:
                self.data = pickle.load(f)
            elapsed = time.time() - start_time
            # Only print this from main process to avoid log spam
            if os.getpid() == getattr(self, "_main_pid", 0) or getattr(self, "_main_pid", 0) == 0:
                print(f"Dataset pickle loaded in {elapsed:.2f}s", flush=True)

            # Pre-convert gate_mapping keys to integers for all samples
            # Pre-convert gate_mapping keys to integers for all samples
            print("Optimizing gate mappings...")
            if self.data:
                # Check if conversion is needed
                need_conversion = False
                for sample in self.data:
                    if "gate_mapping" in sample and sample["gate_mapping"]:
                        first_key = next(iter(sample["gate_mapping"]))
                        if not isinstance(first_key, int):
                            need_conversion = True
                        break

                if need_conversion:
                    from tqdm import tqdm

                    for sample in tqdm(self.data, desc="Converting keys"):
                        if "gate_mapping" in sample and isinstance(sample["gate_mapping"], dict):
                            sample["_gate_mapping_int"] = {
                                int(k): int(v) for k, v in sample["gate_mapping"].items()
                            }
                else:
                    print("Gate mappings already use int keys. Linking directly.")
                    for sample in self.data:
                        if "gate_mapping" in sample:
                            sample["_gate_mapping_int"] = sample["gate_mapping"]

            # Apply max_len filtering
            if self.max_len_filter > 0:
                print(f"Filtering dataset by max_len={self.max_len_filter}...", flush=True)
                original_len = len(self.data)
                new_data = []
                from tqdm import tqdm

                for sample in tqdm(self.data, desc="Filtering"):
                    if "info" in sample and "paths" in sample["info"]:
                        p_lens = [len(p) for p in sample["info"]["paths"]]
                        if max(p_lens) <= self.max_len_filter:
                            new_data.append(sample)
                self.data = new_data
                print(f"Filtered: {original_len} -> {len(self.data)} samples")

            print(f"Loaded {len(self.data)} samples from {dataset_path}")

    @staticmethod
    @lru_cache(maxsize=128)
    def _load_circuit_cached(bench_file: str):
        circuit, _ = parse_bench_file(bench_file)
        return circuit

    def _load_shard_metadata(self, shard_paths: List[str]):
        """Read metadata from shards without loading tensors."""
        self._shard_files = shard_paths
        self._shard_offsets = [0]
        self._shard_lens = []
        total = 0

        # Try to use cached metadata
        import json

        metadata_file = os.path.join(self.processed_dir, "shard_info.json")
        if os.path.exists(metadata_file):
            try:
                with open(metadata_file, "r") as f:
                    meta = json.load(f)
                    self._shard_lens = meta["lens"]
                    self._total_len = sum(self._shard_lens)
                    # Initialize LRU Cache
                    from collections import OrderedDict

                    self._shard_cache = OrderedDict()
                    return
            except Exception:
                print("[WARNING] Failed to load shard_info.json, rescanning...")

        print("Reading shard metadata (one-time scan)...")
        import gc

        for p in tqdm(shard_paths):
            # Load metadata only.
            # Torch 2.0+ supports mmap for faster metadata access without full load
            try:
                # We use weights_only to speed up and minimize object creation
                d = torch.load(p, map_location="cpu", weights_only=True)
                n = d["paths_emb"].shape[0]
                self._shard_lens.append(n)
                total += n
                del d
                gc.collect()  # Force free memory after each shard load during init
            except Exception as e:
                print(f"[ERROR] Failed to read shard {p}: {e}")
                raise

        self._total_len = total
        # Initialize LRU Cache
        from collections import OrderedDict

        self._shard_cache = OrderedDict()

        # Save metadata for next time
        try:
            with open(metadata_file, "w") as f:
                json.dump({"lens": self._shard_lens}, f)
        except Exception:
            pass

    def _get_shard_for_idx(self, idx: int) -> Tuple[int, int]:
        """Find which shard contains the global index."""
        # Binary search or linear scan (linear is fine for <1000 shards)
        for i, length in enumerate(self._shard_lens):
            if idx < length:
                return i, idx
            idx -= length
        raise IndexError(f"Index {idx} out of range for dataset of size {self._total_len}")

    def _ensure_shard_loaded(self, shard_idx: int):
        """Lazy load shard with LRU caching and RAM awareness."""
        if shard_idx in self._shard_cache:
            # Move to end (most recently used)
            self._shard_cache.move_to_end(shard_idx)
            return

        import gc

        import psutil

        # MEMORY AWARENESS: If system RAM is critical, clear local cache before loading more
        mem = psutil.virtual_memory()
        if mem.percent > 90.0:
            # Clear everything to be safe
            self._shard_cache.clear()
            gc.collect()
            print(
                f"[RECONV-MEM] RAM critical ({mem.percent}%). "
                f"Worker {os.getpid()} cleared shard cache."
            )

        # Load new shard
        path = self._shard_files[shard_idx]
        try:
            data = torch.load(path, map_location="cpu")
            self._shard_cache[shard_idx] = data

            # Enforce cache size
            if len(self._shard_cache) > self.cache_size:
                self._shard_cache.popitem(last=False)  # Remove FIFO (Least Recently Used)

        except Exception as e:
            print(f"[Dataset ERROR] Failed to load shard {path}: {e}")
            raise

    def __len__(self) -> int:
        if getattr(self, "_use_processed", False):
            return self._total_len
        return len(self.data)

    def __getitem__(self, idx: int) -> Dict[str, Any]:
        """Get a single dataset item."""
        # Step 1: Initialize metadata containers
        final_info = None
        p_sel, l_sel, v_sel, s_sel = -1, -1, -1, 0

        # Step 2: Load core tensors from Fast Path (shards) or Slow Path (pickle)
        if getattr(self, "_use_processed", False):
            # FAST PATH: Shards
            shard_idx, local_idx = self._get_shard_for_idx(idx)
            self._ensure_shard_loaded(shard_idx)
            data = self._shard_cache.get(shard_idx)
            if data is None:
                raise RuntimeError(f"Shard data is None! shard={shard_idx}")

            paths_emb = data["paths_emb"][local_idx].to(torch.float32).to(self.device)
            attn_mask = data["attn_mask"][local_idx].to(self.device)
            node_ids = data["node_ids"][local_idx].to(self.device)
            file_path = data["files"][local_idx]
            gate_types = data.get("gate_types", torch.full_like(node_ids, -1))[local_idx].to(
                self.device
            )

            if "anchor_p" in data:
                p_sel = int(data["anchor_p"][local_idx].item())
                l_sel = int(data["anchor_l"][local_idx].item())
                v_sel = int(data["anchor_v"][local_idx].item())
                s_sel = int(data["solvability"][local_idx].item())

            # Load pre-baked constraints from shard if available
            if "constraint_mask" in data:
                c_mask_tensor = data["constraint_mask"][local_idx].to(self.device)
                c_vals_tensor = data["constraint_vals"][local_idx].to(self.device)
            else:
                c_mask_tensor = torch.zeros_like(node_ids, dtype=torch.bool)
                c_vals_tensor = torch.zeros_like(node_ids, dtype=torch.long)

            # For non-shard features we may still need pickle info
            if not self.anchor_in_dataset:
                if hasattr(self, "data") and idx < len(self.data):
                    final_info = self.data[idx]["info"]
        else:
            # SLOW PATH: Pickle
            entry = self.data[idx]
            file_path = entry["file"]
            info = entry["info"]
            final_info = info
            has_embeddings = "struct_emb" in entry and "gate_mapping" in entry
            struct_emb = entry.get("struct_emb")
            gate_mapping_int = entry.get("_gate_mapping_int", {})
            paths = info["paths"]
            P = len(paths)
            D = struct_emb.shape[1] if (has_embeddings and struct_emb is not None) else 128
            D_total = D + 3 if self.add_logic_value else D
            max_len = max(len(p) for p in paths)
            L = min(max_len, self.max_path_length)

            # Use float16 to save 50% RAM while on CPU/Disk
            paths_emb = torch.zeros(P, L, D_total, dtype=torch.float16, device=self.device)
            attn_mask = torch.zeros(P, L, dtype=torch.bool, device=self.device)
            node_ids = torch.zeros(P, L, dtype=torch.long, device=self.device)
            gate_types = torch.full((P, L), -1, dtype=torch.long, device=self.device)
            if self.add_logic_value:
                paths_emb[..., -1] = 1.0  # Initialize to Unknown [0, 0, 1]

            circuit = self._load_circuit_cached(file_path)
            # Re-resolve unique nodes for this sample
            unique_nodes = {nid for path in paths for nid in path[:L] if nid > 0}
            node_info = {}
            for nid in unique_nodes:
                gty = int(circuit[nid].type) if nid < len(circuit) else -1
                emb = struct_emb[gate_mapping_int[nid]] if nid in gate_mapping_int else None
                if emb is None:
                    emb = torch.zeros(D) if has_embeddings else torch.randn(D)
                node_info[nid] = (gty, emb)

            for p, path in enumerate(paths):
                for pos in range(min(len(path), L)):
                    nid = path[pos]
                    if nid <= 0:
                        continue
                    node_ids[p, pos], attn_mask[p, pos] = nid, True
                    gty, emb = node_info[nid]
                    gate_types[p, pos], paths_emb[p, pos, :D] = gty, emb

            if self.anchor_in_dataset:
                p_sel, l_sel, v_sel, s_sel = self._gen_anchor_for_sample(
                    node_ids, attn_mask, file_path, info
                )

        # Step 3: Apply anchor hint if present/generated
        if self.add_logic_value and p_sel >= 0 and l_sel >= 0:
            D = paths_emb.shape[-1]
            onehot = torch.tensor(
                [1.0, 0.0, 0.0] if v_sel == 0 else [0.0, 1.0, 0.0], device=self.device
            )
            paths_emb[p_sel, l_sel, D - 3 : D] = onehot

        # Step 4: Use pre-baked constraints (from shard) or empty tensors
        # Constraints are now generated during preprocess_to_shards,
        # not on-the-fly. This eliminates the GIL-locked solver bottleneck.
        if not getattr(self, "_use_processed", False):
            # Slow path (pickle): generate on-the-fly as fallback
            c_mask_tensor = torch.zeros_like(node_ids, dtype=torch.bool, device=self.device)
            c_vals_tensor = torch.zeros_like(node_ids, dtype=torch.long, device=self.device)
            if self.inject_constraints and self.add_logic_value and final_info:
                constraints = self._gen_constraints_for_sample(final_info, file_path)
                if constraints:
                    for nid, val in constraints.items():
                        mask = node_ids == nid
                        if mask.any():
                            c_mask_tensor |= mask
                            c_vals_tensor[mask] = int(val)
                            D = paths_emb.shape[-1]
                            vec = torch.tensor(
                                [1.0, 0.0, 0.0] if val == 0 else [0.0, 1.0, 0.0],
                                device=self.device,
                            )
                            pe_flat = paths_emb.view(-1, D)
                            pe_flat[mask.view(-1), D - 3 : D] = vec
        # else: c_mask_tensor / c_vals_tensor already loaded from shard

        # Step 5: Finalize dictionary
        res = {
            "paths_emb": paths_emb,
            "attn_mask": attn_mask,
            "node_ids": node_ids,
            "gate_types": gate_types,
            "file": file_path,
            "anchor_p": torch.tensor(p_sel, dtype=torch.long, device=self.device),
            "anchor_l": torch.tensor(l_sel, dtype=torch.long, device=self.device),
            "anchor_v": torch.tensor(v_sel, dtype=torch.long, device=self.device),
            "solvability": torch.tensor(s_sel, dtype=torch.long, device=self.device),
        }
        if self.inject_constraints:
            res["constraint_mask"] = c_mask_tensor
            res["constraint_vals"] = c_vals_tensor
        return res

    def _solve_sample_assignment(
        self, info: Dict[str, Any], file_path: str
    ) -> Optional[Dict[int, int]]:
        """Fast solver that uses random PI simulation to get a consistent assignment."""
        from src.atpg.logic_sim_three import logic_sim, reset_gates
        from src.util.struct import GateType, LogicValue

        try:
            circuit = self._load_circuit_cached(file_path)
            if not circuit:
                return None
        except Exception:
            return None

        import random

        # Reset and assign random PIs
        total_gates = len(circuit) - 1
        reset_gates(circuit, total_gates)

        for g in circuit:
            if g and g.type == GateType.INPT:
                g.val = random.choice([LogicValue.ZERO, LogicValue.ONE])

        # Forward simulate
        logic_sim(circuit, total_gates)

        # Build assignment
        return {i: int(circuit[i].val) for i in range(1, total_gates + 1)}

    def _gen_constraints_for_sample(self, info: Dict[str, Any], file_path: str) -> Dict[int, int]:
        """Generate random partial constraints consistent with a valid assignment."""
        assignment = self._solve_sample_assignment(info, file_path)
        if not assignment:
            return {}

        import random

        constraints = {}
        pair_info = info.get("pair_info", {})
        reconv_node = pair_info.get("reconv")

        # Filter assignment to only include nodes relevant to the paths (or PIs)
        # Ideally, we constrain inputs or internal nodes on the paths
        # path_nodes = set()
        # for p in pair_info.get("paths", []):
        #     path_nodes.update(p)

        for nid, val in assignment.items():
            # Don't constrain the target reconvergence node itself (that's the goal!)
            if nid == reconv_node:
                continue

            # Randomly select subset based on probability
            if random.random() < self.constraint_prob:
                constraints[nid] = int(val)

        return constraints

    def _gen_anchor_for_sample(
        self,
        node_ids: torch.Tensor,
        attn_mask: torch.Tensor,
        file_path: str,
        info: Dict[str, Any],
    ) -> Tuple[int, int, int, int]:
        """
        Generate anchor hint for a single sample using PathConsistencySolver.
        Returns: (path_idx, pos_idx, value, solvability)

        - path_idx: Which path contains the anchor (-1 if no anchor)
        - pos_idx: Position within the path (-1 if no anchor)
        - value: Logic value (0 or 1) at anchor point (-1 if no anchor)
        - solvability: 1 if structure is SAT, 0 if UNSAT or unknown
        """
        from src.util.struct import LogicValue

        if "pair_info" in info:
            pair_info = info["pair_info"]
        elif all(k in info for k in ["start", "reconv", "paths"]):
            pair_info = info
        else:
            return -1, -1, -1, 0

        # Try to solve for valid assignment using helper
        assignment = self._solve_sample_assignment(info, file_path)

        # If no solution found, mark as UNSAT
        if assignment is None:
            # Try using the helper (which also tries 1 then 0)
            assignment_dict = self._solve_sample_assignment(info, file_path)
            if not assignment_dict:
                return -1, -1, -1, 0
            # Convert dict back to LogicValue for compatibility if needed,
            # but helper returns int/LogicValue mixed?
            # Helper returns whatever solver returns (LogicValue enum usually).
            assignment = assignment_dict

        # If no solution found (should be caught above), mark as UNSAT
        if assignment is None:
            return -1, -1, -1, 0

        # Solvable! Now select an intelligent anchor point
        # Strategy: Pick a node from the middle 50% of any path
        paths = pair_info["paths"]
        path_node_sets = [set(p) for p in paths]

        # Find nodes in assignment that are on paths
        candidates = []
        for node_id, logic_val in assignment.items():
            if logic_val in [LogicValue.ZERO, LogicValue.ONE]:
                # Check which path contains this node
                for p_idx, node_set in enumerate(path_node_sets):
                    if node_id in node_set:
                        # Find position in path
                        try:
                            l_idx = paths[p_idx].index(node_id)
                            path_len = len(paths[p_idx])
                            # Prefer middle 50% of path
                            rel_pos = l_idx / max(1, path_len - 1)
                            if 0.25 <= rel_pos <= 0.75:
                                priority = 1.0
                            else:
                                priority = 0.5

                            candidates.append(
                                {
                                    "p_idx": p_idx,
                                    "l_idx": l_idx,
                                    "value": 1 if logic_val == LogicValue.ONE else 0,
                                    "priority": priority,
                                }
                            )
                        except ValueError:
                            continue

        # Select best candidate
        if not candidates:
            # No good anchor found, but structure is solvable
            return -1, -1, -1, 1

        # Sort by priority (prefer middle positions)
        candidates.sort(key=lambda x: x["priority"], reverse=True)
        best = candidates[0]

        return best["p_idx"], best["l_idx"], best["value"], 1

