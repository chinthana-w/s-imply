    def _locate_shard(self, global_idx: int) -> Tuple[int, int]:
        acc = 0
        for i, sz in enumerate(self._shard_sizes):
            if global_idx < acc + sz:
                return i, global_idx - acc
            acc += sz
        raise IndexError(global_idx)

    @staticmethod
    def preprocess_to_shards(
        input_dataset_path: str,
        output_dir: str,
        max_path_length: int = 50,
        shard_size: int = 5000,
        dtype: torch.dtype = torch.float16,
        device: torch.device = torch.device("cpu"),
    ) -> None:
        """Precompute per-sample tensors and save to shard files for fast training.

        This will read the pickle dataset (with struct_emb + gate_mapping), build
        paths_emb/attn_mask/node_ids for each sample (padded to max_path_length),
        and write them in shards to output_dir as shard_XXXXX.pt files.
        """
        os.makedirs(output_dir, exist_ok=True)

        # Use the same mapping logic via a temporary dataset instance (raw mode)
        raw_ds = ReconvergentPathsDataset(
            input_dataset_path,
            device=device,
            max_path_length=max_path_length,
            load_processed=False,
        )

        total = len(raw_ds)
        shard_idx = 0

        buf_paths: List[torch.Tensor] = []
        buf_masks: List[torch.Tensor] = []
        buf_nodes: List[torch.Tensor] = []
        buf_files: List[str] = []

        # Buffers for new fields
        buf_ap: List[torch.Tensor] = []
        buf_al: List[torch.Tensor] = []
        buf_av: List[torch.Tensor] = []
        buf_solv: List[torch.Tensor] = []
        buf_gt: List[torch.Tensor] = []
        buf_cmask: List[torch.Tensor] = []
        buf_cvals: List[torch.Tensor] = []

        def flush() -> None:
            nonlocal shard_idx, buf_paths, buf_masks, buf_nodes, buf_files
            nonlocal buf_ap, buf_al, buf_av, buf_solv, buf_gt
            nonlocal buf_cmask, buf_cvals
            if not buf_paths:
                return
            # Final safety: ensure equal shapes before stacking
            base_shape = buf_paths[0].shape
            for j, t in enumerate(buf_paths):
                if t.shape != base_shape:
                    msg = (
                        f"Inconsistent sample shape within shard buffer: \n"
                        f"idx 0 {base_shape} vs idx {j} {t.shape}"
                    )
                    raise RuntimeError(msg)
            paths_t = torch.stack(buf_paths, dim=0).to(dtype)
            masks_t = torch.stack(buf_masks, dim=0)
            nodes_t = torch.stack(buf_nodes, dim=0)

            # Stack anchor fields
            ap_t = torch.tensor(buf_ap, dtype=torch.long)
            al_t = torch.tensor(buf_al, dtype=torch.long)
            av_t = torch.tensor(buf_av, dtype=torch.long)
            sv_t = torch.tensor(buf_solv, dtype=torch.long)
            gt_t = torch.stack(buf_gt, dim=0)
            cmask_t = torch.stack(buf_cmask, dim=0)
            cvals_t = torch.stack(buf_cvals, dim=0)

            shard = {
                "paths_emb": paths_t.cpu(),
                "attn_mask": masks_t.cpu(),
                "node_ids": nodes_t.cpu(),
                "files": list(buf_files),
                "anchor_p": ap_t.cpu(),
                "anchor_l": al_t.cpu(),
                "anchor_v": av_t.cpu(),
                "solvability": sv_t.cpu(),
                "gate_types": gt_t.cpu(),
                "constraint_mask": cmask_t.cpu(),
                "constraint_vals": cvals_t.cpu(),
            }
            out_path = os.path.join(output_dir, f"shard_{shard_idx:05d}.pt")
            torch.save(shard, out_path)
            print(f"Wrote {out_path} ({paths_t.shape[0]} samples)")
            shard_idx += 1
