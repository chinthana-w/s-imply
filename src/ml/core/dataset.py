def _inject_anchor_into_embeddings(
    paths: torch.Tensor,
    anchor_p: torch.Tensor,
    anchor_l: torch.Tensor,
    anchor_v: torch.Tensor,
    enable: bool = True,
) -> torch.Tensor:
    """
    Inject anchor one-hot vector into the last 3 channels of embeddings.
    """
    if not enable:
        return paths

    B, P, L, D = paths.shape
    if D < 3:
        return paths

    # We need to scatter.
    # Iterate for simplicity since B is small-ish (128).
    # Vectorized is better.

    # paths[b, p, l, -3:] = one_hot

    # Filter valid anchors
    valid = (anchor_p >= 0) & (anchor_l >= 0)
    if not valid.any():
        return paths

    # Creating a mask index is tricky with 4D tensor.
    # Clone to avoid in-place issues if needed?
    out = paths.clone()

    # Loop over valid indices
    idxs = torch.nonzero(valid).squeeze(1)
    for b in idxs:
        p = anchor_p[b].item()
        l_val = anchor_l[b].item()
        v = anchor_v[b].item()

        # One-hot: 0 -> [1,0,0], 1 -> [0,1,0], X -> [0,0,1]
        # X is default.
        if v == 0:
            vec = torch.tensor([1.0, 0.0, 0.0], device=paths.device)
        elif v == 1:
            vec = torch.tensor([0.0, 1.0, 0.0], device=paths.device)
        else:
            vec = torch.tensor([0.0, 0.0, 1.0], device=paths.device)

        out[b, p, l_val, D - 3 :] = vec

    return out
