def resolve_gate_types(
    node_ids: torch.Tensor, files: List[str], device: torch.device
) -> torch.Tensor:
    """
    Resolve gate types for a batch of node IDs using cached circuit parsing.
    Returns [B, P, L] tensor of gate types.
    """
    gates = torch.full_like(node_ids, -1, device=device)
    # This is slow if batch size is large and files are diverse;
    # meant as fallback if gate_types not in batch.

    # Group by file to minimize parsing
    file_map = {}
    for b, f in enumerate(files):
        if f not in file_map:
            file_map[f] = []
        file_map[f].append(b)

    for f, indices in file_map.items():
        circuit = ReconvergentPathsDataset._load_circuit_cached(f)
        for b in indices:
            # node_ids[b] is [P, L]
            # iterate and map
            # Vectorized mapping is hard without a dense map tensor for the whole circuit,
            # which we don't have readily available in a tensor form here.
            # So we iterate. This is slow!

            # Optimization: Move scalar loop to CPU
            nb = node_ids[b].cpu()
            gb = gates[b].cpu()

            # We can map using a lookup
            # But simplest is loop.
            P, L = nb.shape
            for p in range(P):
                for length in range(L):
                    nid = int(nb[p, length].item())
                    if nid > 0 and nid < len(circuit):
                        gb[p, length] = int(circuit[nid].type)

            gates[b] = gb.to(device)

    return gates


def generate_constraints(
    node_ids: torch.Tensor, files: List[str], prob: float = 0.5
) -> Tuple[torch.Tensor, torch.Tensor]:
    """
    Generate random constraints for training.
    Returns:
        mask: [B, P, L] bool, True where constraint is applied
        vals: [B, P, L] int (0 or 1), the target value
    """
    if prob <= 0:
        return (
            torch.zeros_like(node_ids, dtype=torch.bool),
            torch.zeros_like(node_ids, dtype=torch.long),
        )

    # Random mask
    mask = (torch.rand_like(node_ids, dtype=torch.float) < prob) & (node_ids > 0)

    # Random values (0 or 1)
    # In a real constrained task, we might want *consistent* values,
    # but for "Constrained Path Training" to robustify the model,
    # random constraints force the model to adapt.
    vals = torch.randint_like(node_ids, 0, 2)

    return mask, vals


