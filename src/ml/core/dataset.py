def reconv_collate(batch: List[Dict[str, Any]], max_paths: int = 0) -> Dict[str, Any]:
    """Collate variable-length samples into a batch.

    Parameters
    ----------
    batch : list[dict]
        List of samples from ReconvergentPathsDataset.

    Returns
    -------
    dict
        Batched dictionary with keys:
        - paths_emb: [B, P, L, D] batched path embeddings
        - attn_mask: [B, P, L] batched attention masks
        - node_ids: [B, P, L] batched node IDs
        - files: list[str] file paths for each sample in batch
    """
    if not batch:
        raise ValueError("Cannot collate empty batch")

    device = batch[0]["paths_emb"].device

    # Find maximum dimensions across the batch
    P = max(item["paths_emb"].size(0) for item in batch)
    L = max(item["paths_emb"].size(1) for item in batch)
    D_raw = batch[0]["paths_emb"].size(2)
    B = len(batch)

    # Pad embedding dimension to be divisible by 4 (common nhead value)
    # This ensures transformer compatibility
    nhead = 4
    if D_raw % nhead == 0:
        D = D_raw
    else:
        D = ((D_raw // nhead) + 1) * nhead

    # Truncate to max_paths (prevent OOM)
    # Default 0 means no truncation (or use MAX_PATHS if passed)
    if max_paths > 0:
        MAX_PATHS = max_paths
        if P > MAX_PATHS:
            P = MAX_PATHS

    # Initialize batch tensors — use float16 to match source dtype from shards
    # Converted to float32 once at the end before returning to model
    src_dtype = batch[0]["paths_emb"].dtype
    paths = torch.zeros(B, P, L, D, device=device, dtype=src_dtype)
    masks = torch.zeros(B, P, L, dtype=torch.bool, device=device)
    node_ids = torch.zeros(B, P, L, dtype=torch.long, device=device)
    gate_types = torch.full((B, P, L), -1, dtype=torch.long, device=device)
    files: List[str] = []

    # Fill in data from each sample
    for b, item in enumerate(batch):
        p_i, l_i, d = item["paths_emb"].shape
        p_curr = min(p_i, P)

        # Copy embeddings (padding happens automatically via zeros initialization)
        paths[b, :p_curr, :l_i, :d] = item["paths_emb"][:p_curr]
        masks[b, :p_curr, :l_i] = item["attn_mask"][:p_curr]
        node_ids[b, :p_curr, :l_i] = item["node_ids"][:p_curr]
        if "gate_types" in item:
            gate_types[b, :p_curr, :l_i] = item["gate_types"][:p_curr]
        files.append(item["file"])

    # Convert to float32 for model computation
    paths = paths.to(torch.float32)

    out: Dict[str, Any] = {
        "paths_emb": paths,
        "attn_mask": masks,
        "node_ids": node_ids,
        "gate_types": gate_types,
        "files": files,
    }
    # Collate optional anchors if present
    if (
        "anchor_p" in batch[0]
        and "anchor_l" in batch[0]
        and "anchor_v" in batch[0]
        and "solvability" in batch[0]
    ):
        ap = torch.stack([item["anchor_p"] for item in batch], dim=0)
        al = torch.stack([item["anchor_l"] for item in batch], dim=0)
        av = torch.stack([item["anchor_v"] for item in batch], dim=0)
        sv = torch.stack([item["solvability"] for item in batch], dim=0)
        out["anchor_p"] = ap
        out["anchor_l"] = al
        out["anchor_v"] = av
        out["solvability"] = sv
    return out


